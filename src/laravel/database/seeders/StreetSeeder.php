    /**
     * @param string $cityName
     * @param string[] $streets
     * @return array<array{name: string, city_id: int}>
     */
    private function prepareCityStreetsData(string $cityName, array $streets): array
    {
        $cityId = City::where('name', $cityName)->firstOrFail()->id;

        return array_map(function ($street) use ($cityId) {
            return [
              'name' => $street,
              'city_id' => $cityId,
            ];
        }, $streets);
    }

